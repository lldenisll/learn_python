'O while executa comandos enquanto uma ou mais condições forem verdadeiras, muito utilizado para evitar repetições nos códigos, sendo while + condição: lembrar dos espaços na linha de baixo, é importante utilizar variáveis no exemplo abaixo o i é a variável, e irá elevar o 2 ao i até o que está na condição no caso até a potencia 10'
i=0

while i<=10: 
    print(2**i)
    i=i+1

x=0
